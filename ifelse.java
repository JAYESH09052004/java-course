public class ifelse {
    public static void main(String[] args) {
        int marks = 55;
        if (marks == 35) {
            System.out.println("fair");
        } else if (marks > 60) {
            System.out.println("good");
        } else if (marks >=90) {
            System.out.println("excellent");
        } else if (marks < 25) {
            System.out.println("fail");

        }
    }
}

